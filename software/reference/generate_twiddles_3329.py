#!/usr/bin/env python3
"""Generate and verify the ML-KEM q=3329 twiddle ROM.

The official CRYSTALS-Kyber reference implementation stores signed
Montgomery-domain zetas. This repository's current butterfly multiplier is a
standard modular multiplier, so this script converts those constants to
canonical standard-domain representatives before emitting RTL and test vectors.

Source constants:
https://github.com/pq-crystals/kyber/blob/main/ref/ntt.c
"""

from __future__ import annotations

