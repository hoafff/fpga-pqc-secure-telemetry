def derive_standard_twiddles() -> tuple[int, ...]:
    """Return canonical standard-domain zetas in official schedule order."""
    if len(BIT_REVERSED_TREE) != 128:
        raise AssertionError("bit-reversed tree must contain 128 entries")
    if sorted(BIT_REVERSED_TREE) != list(range(128)):
        raise AssertionError("bit-reversed tree must be a permutation of 0..127")
    if len(OFFICIAL_MONTGOMERY_ZETAS_SIGNED) != 128:
        raise AssertionError("official zeta table must contain 128 entries")

    r_inverse = pow(MONTGOMERY_R, -1, Q)
    standard: list[int] = []

    for index, exponent in enumerate(BIT_REVERSED_TREE):
        expected_standard = pow(ROOT_OF_UNITY, exponent, Q)
        official_montgomery = OFFICIAL_MONTGOMERY_ZETAS_SIGNED[index] % Q
        expected_montgomery = (expected_standard * MONTGOMERY_R) % Q

        if official_montgomery != expected_montgomery:
            raise AssertionError(
                "official Montgomery zeta mismatch at "
                f"index={index}: got={official_montgomery}, "
                f"expected={expected_montgomery}"
            )

        converted = (official_montgomery * r_inverse) % Q
        if converted != expected_standard:
            raise AssertionError(
                "standard-domain conversion mismatch at "
                f"index={index}: got={converted}, expected={expected_standard}"
            )
        if not 0 <= converted < Q:
            raise AssertionError(f"non-canonical twiddle at index={index}")

        standard.append(converted)

    if standard[0] != 1:
        raise AssertionError("address 0 must contain the neutral twiddle 1")

    return tuple(standard)


def render_hex(values: tuple[int, ...]) -> str:
    return "".join(f"{value:04x}\n" for value in values)


def render_systemverilog(values: tuple[int, ...]) -> str:
    case_lines = "\n".join(
        f"            7'd{index}: twiddle_lookup = 16'd{value};"
        for index, value in enumerate(values)
    )

    return f"""// Generated by software/reference/generate_twiddles_3329.py.
// Do not edit this file manually.
//
// Twiddles are canonical standard-domain values in [0, 3328].
// The official reference's signed Montgomery-domain zetas are converted
// because this repository currently uses standard modular multiplication.

module twiddle_rom_3329 (
    input  logic        clk_i,
    input  logic        rst_ni,
    input  logic        valid_i,
    input  logic [6:0]  addr_i,
    output logic        valid_o,
    output logic [15:0] zeta_o
);
    logic       valid_s1;
    logic [6:0] addr_s1;

    function automatic logic [15:0] twiddle_lookup(
        input logic [6:0] addr
    );
        begin
            case (addr)
{case_lines}
                default: twiddle_lookup = 16'd0;
            endcase
        end
    endfunction

    always_ff @(posedge clk_i) begin
        if (!rst_ni) begin
            valid_s1 <= 1'b0;
            valid_o  <= 1'b0;
            addr_s1  <= '0;
            zeta_o   <= '0;
        end else begin
            valid_s1 <= valid_i;
            valid_o  <= valid_s1;

            if (valid_i)
                addr_s1 <= addr_i;

            if (valid_s1)
                zeta_o <= twiddle_lookup(addr_s1);
        end
    end
endmodule
"""


def write_or_check(path: Path, expected: str, check_only: bool) -> bool:
    """Write a generated file or verify that its committed contents match."""
    if check_only:
        if not path.exists():
            print(f"ERROR: missing generated file: {path}", file=sys.stderr)
            return False

        actual = path.read_text(encoding="utf-8")
        if actual != expected:
            print(
                f"ERROR: generated file is stale: {path}\n"
                "Run: python3 software/reference/generate_twiddles_3329.py --write",
                file=sys.stderr,
            )
            return False
        return True

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(expected, encoding="utf-8")
    print(f"WROTE: {path}")
    return True


def main() -> int:
    parser = argparse.ArgumentParser()
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--write", action="store_true", help="regenerate committed files")
    mode.add_argument("--check", action="store_true", help="verify committed files")
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[2]
    values = derive_standard_twiddles()

    targets = (
        (
            repo_root / "rtl/ntt/twiddle_rom_3329.sv",
            render_systemverilog(values),
        ),
        (
            repo_root / "tb/vectors/twiddle_3329_standard.hex",
            render_hex(values),
        ),
    )

    ok = all(
        write_or_check(path, expected, args.check)
        for path, expected in targets
    )
    if not ok:
        return 1

    action = "verified" if args.check else "generated"
    print(
        f"PASS: {action} 128 q=3329 standard-domain twiddles; "
        "forward schedule uses addresses 1..127, inverse uses 127..1"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
